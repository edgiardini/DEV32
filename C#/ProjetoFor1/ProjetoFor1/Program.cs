using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ProjetoFor1
{
    public class Program
    {
        static void Main(string[] args)
        {// trabalhando com estrutura DE repetição FOR
            // for = repetição = laços = iteração = loops = voltas
            for (int k = 1; k <= 10; k++) //k=k+1
            {
                Console.WriteLine($"Número: {k}");
            }
            Console.ReadKey();
        }
    }
}
